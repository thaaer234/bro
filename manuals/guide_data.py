def _build_control_summary(controls):
    counts = {}
    for control in controls:
        kind = control.get("kind") or "other"
        counts[kind] = counts.get(kind, 0) + 1
    return [
        {"kind": kind, "count": count}
        for kind, count in sorted(counts.items(), key=lambda item: (-item[1], item[0]))
    ]


def _filter_controls(controls, query):
    if not query:
        return controls
    filtered = []
    for control in controls:
        haystack = " ".join(
            [
                control.get("kind", ""),
                control.get("label", ""),
                control.get("target", ""),
                control.get("meta", ""),
            ]
        ).lower()
        if query in haystack:
            filtered.append(control)
    return filtered


