def _build_template_steps(template):
    label = template["label"]
    controls = template["controls"]
    control_summary = {item["kind"]: item["count"] for item in template["control_summary"]}
    steps = [f"ابدأ من صفحة {label} واقرأ عنوانها ومحتواها العلوي لتتأكد أنك داخل الشاشة الصحيحة لهذا الإجراء."]
    if control_summary.get("نموذج"):
        steps.append("إذا كانت الصفحة تحتوي نموذجاً، عبئ الحقول المطلوبة أولاً وراجعها قبل تنفيذ أي حفظ أو إرسال.")
    if control_summary.get("إدخال") or any("بحث" in control.get("label", "") or "filter" in (control.get("target", "") or "").lower() for control in controls):
        steps.append("استخدم البحث أو الفلاتر أولاً لتقليل النتائج والوصول إلى السجل الصحيح قبل المتابعة.")
    if control_summary.get("رابط"):
        steps.append("بعد تحديد السجل أو التقرير المناسب استخدم الروابط الظاهرة للانتقال إلى التفاصيل أو الصفحة التابعة.")
    if any(_infer_control_category(control) in {"save", "create", "edit", "danger"} for control in controls):
        steps.append("أنهِ العملية بزر الحفظ أو التأكيد المناسب، ولا تستخدم الحذف أو الإغلاق إلا بعد مراجعة البيانات.")
    if any(_infer_control_category(control) in {"output", "report"} for control in controls):
        steps.append("عند الحاجة لنسخة نهائية استخدم الطباعة أو التصدير بعد ضبط الفلاتر والنتائج.")
    deduped = []
    seen = set()
    for step in steps:
        if step in seen:
            continue
        seen.add(step)
        deduped.append(step)
    return deduped[:5]


def _merge_template_manual_override(relative_path, template_data):
    override = TEMPLATE_MANUAL_OVERRIDES.get(relative_path)
    screenshot = TEMPLATE_SCREENSHOTS.get(relative_path)
    if not override and not screenshot:
        return template_data
    merged = {**template_data}
    if override:
        if override.get("intro"):
            merged["intro"] = override["intro"]
        if override.get("steps"):
            merged["steps"] = override["steps"]
        control_overrides = override.get("controls", {})
        if control_overrides and merged.get("controls"):
            updated_controls = []
            for control in merged["controls"]:
                match_override = None
                for key, value in control_overrides.items():
                    if key and key in control.get("label", ""):
                        match_override = value
                        break
                if match_override:
                    updated_controls.append({**control, **match_override})
                else:
                    updated_controls.append(control)
            merged["controls"] = updated_controls
    if screenshot:
        merged["screenshot"] = screenshot
    return merged


def _filter_template_groups(template_groups, query="", selected_group=""):
    filtered_groups = []
    for group in template_groups:
        if selected_group and group["title"] != selected_group:
            continue
        templates = []
        for template in group["templates"]:
            filtered_controls = _filter_controls(template["controls"], query)
            template_haystack = " ".join(
                [
                    template["label"],
                    template["path"],
                    template["used_by"],
                    template["purpose"],
                    template["when"],
                ]
            ).lower()
            if query and query not in template_haystack and not filtered_controls:
                continue
            active_controls = filtered_controls if query else template["controls"]
            templates.append(
                {
                    **template,
                    "controls": active_controls,
                    "control_count": len(active_controls),
                    "control_summary": _build_control_summary(active_controls),
                    "steps": _build_template_steps(
                        {
                            **template,
                            "controls": active_controls,
                            "control_summary": _build_control_summary(active_controls),
                        }
                    ),
                }
            )
        if not templates:
            continue
        filtered_groups.append(
            {
                **group,
                "anchor": _slugify_group_name(group["title"]),
                "display_title": group.get("display_title", GROUP_TITLES.get(group["title"], group["title"])),
                "template_count": len(templates),
                "control_count": sum(item["control_count"] for item in templates),
                "templates": templates,
            }
        )
    return filtered_groups


def _screen_group_key(screen):
    path = (screen.get("path") or "").lower()
    if path.startswith("/quick/"):
        return "quick"
    if path.startswith("/accounts/"):
        return "accounts"
    if path.startswith("/students/"):
        return "students"
    if path.startswith("/attendance/"):
        return "attendance"
    if path.startswith("/employ/"):
        return "employ"
    if path.startswith("/announcements/"):
        return "announcements"
    if path.startswith("/pages/") or path.startswith("/login/"):
        return "pages"
    return "shared"


def _build_app_coverage(route_groups, template_groups):
    route_map = {
        "الحساب والدخول": "registration",
        "الصفحات العامة والإدارة": "pages",
        "الطلاب النظاميون": "students",
        "النظام السريع": "quick",
        "الحضور": "attendance",
        "الشعب الدراسية": "classroom",
        "الاختبارات": "exams",
        "المحاسبة": "accounts",
        "الموارد البشرية والمدرسون": "employ",
        "المواد الدراسية": "courses",
        "التعاميم": "announcements",
        "الأمن والأخطاء": "errors",
    }
    route_index = {route_map.get(group["title"], group["title"]): group for group in route_groups}
    screen_counts = {}
    button_counts = {}
    for screen in USER_MANUAL_SCREENS:
        key = _screen_group_key(screen)
        screen_counts[key] = screen_counts.get(key, 0) + 1
        button_counts[key] = button_counts.get(key, 0) + len(screen.get("buttons", []))

    coverage = []
    all_keys = sorted({group["title"] for group in template_groups} | set(route_index.keys()) | set(screen_counts.keys()))
    for key in all_keys:
        template_group = next((group for group in template_groups if group["title"] == key), None)
        route_group = route_index.get(key)
        coverage.append(
            {
                "title": key,
                "display_title": GROUP_TITLES.get(key, key),
                "anchor": _slugify_group_name(key),
                "template_count": template_group["template_count"] if template_group else 0,
                "control_count": template_group["control_count"] if template_group else 0,
                "documented_screen_count": screen_counts.get(key, 0),
                "documented_button_count": button_counts.get(key, 0),
                "route_count": route_group["count"] if route_group else 0,
                "description": (template_group or {}).get("description", f"تغطية تشغيلية لتطبيق {key}."),
            }
        )
    return coverage


def _departments_for_groups(group_keys):
    departments = []
    seen = set()
    for key in group_keys:
        label = GROUP_DEPARTMENT_MAP.get(key)
        if not label or label in seen:
            continue
        seen.add(label)
        departments.append(label)
    return departments


def _permission_codes_for_user(user):
    if not user or not getattr(user, "is_authenticated", False):
        return set()
    if getattr(user, "is_superuser", False):
        return {"*"}
    employee = getattr(user, "employee_profile", None)
    if not employee:
        return set()
    return set(employee.permissions.filter(is_granted=True).values_list("permission", flat=True))


def _allowed_group_keys_for_user(user):
    if not user or not getattr(user, "is_authenticated", False):
        return set(COMMON_MANUAL_GROUPS)
    if getattr(user, "is_superuser", False):
        all_groups = {group["title"] for group in build_template_inventory()}
        all_groups.update({"announcements", "errors", "registration", "pages", "manuals"})
        return all_groups
    allowed = set(COMMON_MANUAL_GROUPS)
    permission_codes = _permission_codes_for_user(user)
    for code in permission_codes:
        for prefix, groups in PERMISSION_GROUP_MAP.items():
            if code.startswith(prefix):
                allowed.update(groups)
    return allowed


def _filter_groups_by_keys(groups, allowed_keys):
    return [group for group in groups if group["title"] in allowed_keys]


def _filter_screens_for_groups(screens, allowed_keys):
    filtered = []
    for screen in screens:
        allowed_groups = USER_MANUAL_SCREEN_GROUPS.get(screen.get("slug"), {_screen_group_key(screen)})
        if allowed_groups & set(allowed_keys) or _screen_group_key(screen) == "shared":
            filtered.append(screen)
    return filtered


def _screen_visible_for_user(screen, user, allowed_keys, permission_codes):
    slug = screen.get("slug")
    if getattr(user, "is_superuser", False):
        return True
    if slug in {"login-screen", "sidebar"}:
        return True
    if slug == "announcements":
        return False
    required_any_codes = USER_MANUAL_SCREEN_REQUIRED_ANY_CODES.get(slug)
    if required_any_codes:
        return bool(required_any_codes & set(permission_codes))
    required_prefixes = USER_MANUAL_SCREEN_PERMISSION_PREFIXES.get(slug)
    if required_prefixes:
        return any(
            code.startswith(prefix)
            for prefix in required_prefixes
            for code in permission_codes
        )
    allowed_groups = USER_MANUAL_SCREEN_GROUPS.get(slug, {_screen_group_key(screen)})
    return bool(allowed_groups & set(allowed_keys))


def _is_noise_template_for_user_manual(template):
    path = (template.get("path") or "").lower()
    name = Path(path).name.lower()
    label = (template.get("label") or "").strip()
    if any(token in path for token in USER_MANUAL_TEMPLATE_EXCLUDE_TOKENS):
        return True
    if name in USER_MANUAL_TEMPLATE_EXCLUDE_NAMES:
        return True
    if name.startswith("base") or name.startswith("_"):
        return True
    if not label:
        return True
    if re.fullmatch(r"[\W_()/:-]+", label):
        return True
    return False


def _is_curated_template_for_user_manual(template):
    path = template.get("path") or ""
    if _is_noise_template_for_user_manual(template):
        return False
    if path in TEMPLATE_MANUAL_OVERRIDES:
        return True
    if path in TEMPLATE_SCREENSHOTS:
        return True
    return False


def _filter_template_groups_for_user(groups, allowed_keys):
    filtered_groups = []
