@lru_cache(maxsize=1)
def build_template_inventory():
    groups = {}
    skip_roots = {"_tmp_templates_zip", "_tmp_pdf", "__pycache__"}
    for template_path in sorted(Path(".").rglob("templates/**/*.html")):
        if any(part in skip_roots for part in template_path.parts):
            continue
        if " copy" in template_path.name.lower():
            continue
        try:
            text = template_path.read_text(encoding="utf-8")
        except Exception:
            continue
        controls = _extract_controls_from_template(text)
        group_name = _template_group_for_path(template_path)
        groups.setdefault(
            group_name,
            {
                "key": group_name,
                "title": group_name,
                "display_title": GROUP_TITLES.get(group_name, group_name),
                "description": GROUP_DESCRIPTIONS.get(group_name, f"قوالب التطبيق {group_name} داخل النظام."),
                "template_count": 0,
                "control_count": 0,
                "templates": [],
            },
        )
        relative = template_path.as_posix()
        primary_heading = _extract_primary_heading(text)
        label = primary_heading or _prettify_template_name(template_path.stem)
        used_by = GROUP_USER_HINTS.get(group_name, "المستخدم المرتبط بهذا التطبيق")
        purpose = _infer_template_purpose(template_path.stem, group_name)
        when = _infer_template_when(template_path.stem)
        described_controls = [
            _describe_control(control, index, used_by, when)
            for index, control in enumerate(controls)
        ]
        control_summary = _build_control_summary(described_controls)
        groups[group_name]["template_count"] += 1
        groups[group_name]["control_count"] += len(controls)
        template_data = {
            "name": template_path.name,
            "label": label,
            "path": relative,
            "control_count": len(controls),
            "used_by": used_by,
            "purpose": purpose,
            "when": when,
            "intro": _infer_template_intro(label, purpose, when),
            "controls": described_controls,
            "control_summary": control_summary,
            "steps": _build_template_steps(
                {
                    "label": label,
                    "controls": described_controls,
                    "control_summary": control_summary,
                }
            ),
        }
        groups[group_name]["templates"].append(_merge_template_manual_override(relative, template_data))
    ordered = sorted(groups.values(), key=lambda item: item["title"])
    return ordered


def build_manuals_context(query="", selected_group=""):
    route_groups = [
        {
            **group,
            "anchor": _slugify_group_name(group["title"]),
        }
        for group in build_route_inventory()
    ]
    template_groups = build_template_inventory()
    focus_group_names = {"quick", "accounts", "students", "attendance", "employ"}
    filtered_template_groups = _filter_template_groups(template_groups, query=query, selected_group=selected_group)
    app_coverage = _build_app_coverage(route_groups, template_groups)
    focus_template_groups = [group for group in filtered_template_groups if group["title"] in focus_group_names]
    non_focus_template_groups = [group for group in filtered_template_groups if group["title"] not in focus_group_names]
    route_total = sum(group["count"] for group in route_groups)
    button_total = sum(len(screen["buttons"]) for screen in USER_MANUAL_SCREENS)
    template_total = sum(group["template_count"] for group in filtered_template_groups)
    template_control_total = sum(group["control_count"] for group in filtered_template_groups)
    return {
        "manual_workflows": USER_MANUAL_WORKFLOWS,
        "manual_screens": USER_MANUAL_SCREENS,
        "manual_roles": USER_MANUAL_ROLES,
        "manual_errors": USER_MANUAL_ERRORS,
        "manual_route_groups": route_groups,
        "manual_app_coverage": app_coverage,
        "manual_template_groups": filtered_template_groups,
        "manual_focus_template_groups": focus_template_groups,
        "manual_non_focus_template_groups": non_focus_template_groups,
        "manual_group_options": [{"value": group["title"], "label": group.get("display_title", group["title"])} for group in template_groups],
        "manual_selected_group": selected_group,
        "manual_search_query": query,
        "manual_has_active_filters": bool(query or selected_group),
        "manual_route_total": route_total,
        "manual_button_total": button_total,
        "manual_screen_total": len(USER_MANUAL_SCREENS),
        "manual_template_total": template_total,
        "manual_template_control_total": template_control_total,
    }
